package GA4;
/*
 * object creation
 * 
 * @param val. value
 * 
 * @return none
 */
public class Value {
	//initialize value
	private int val;
	/*
	 * object creation
	 * 
	 * @param val. value
	 * 
	 * @return none
	 */
	public Value(int val) {
		this.val = val;
	}
	/*
	 * get value
	 * 
	 * @param none
	 * 
	 * @return val. value
	 */
	public int getVal() {
		return val;
	}
	/*
	 * set value
	 * 
	 * @param val. value
	 * 
	 * @return none
	 */
	public void setVal(int val) {
		this.val = val;
	}	
}
